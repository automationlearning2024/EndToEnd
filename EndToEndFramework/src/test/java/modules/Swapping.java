package modules;

public class Swapping {

	public static void main(String[] args) {

		System.out.println("test");

		int a=10;  // swap == a= 15 done
		
		int b=15;  // b= 10
		
		int temp;  // 10
		
		       temp=a;
		       
		         a = b;
		          
		              b=temp;
		              
		              System.out.println(a);
		              System.out.println(b);
		       
		
		

	}

}
